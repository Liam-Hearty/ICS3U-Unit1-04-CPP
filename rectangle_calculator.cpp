// This program will calculate the Area and Perimeter of a 3mm by 5mm rectangle


#include <iostream>
#include <cmath>

int main() {
    std::cout << "If a rectangle has the dimensions: 3mm x 5mm" << std::endl;
    std::cout << "What are the area/perimeter of the rectangle?" << std::endl;
    std::cout << "Area=" << (3*5) << std::endl;
    std::cout << "Perimeter=" << (2*(3+5)) << std::endl;
}
